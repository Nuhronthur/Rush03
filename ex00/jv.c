int	ft_atoi(char *str)
{
	int	i;
	int	m;
	int	r;

	i = 0;
	m = 0;
	r = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			m++;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		r = r * 10 + (str[i] - '0');
		i++;
	}
	if (m % 2 == 1)
		r *= -1;
	return (r);
}

